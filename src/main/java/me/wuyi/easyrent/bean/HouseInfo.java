package me.wuyi.easyrent.bean;

public class HouseInfo {
    private String oneLevelDistrict;
    private String twoLevelRegion;
    private String houseType;
    private String sourceFrom;
    private String rentType;
    private String roomType;
    private String genderLimit;
    private String houseLink;
    private String topicDesc;
    private String postDate;
    private String houseCity;
    private String metroNearBy;

    public HouseInfo() {
    }

    public String getOneLevelDistrict() {
        return oneLevelDistrict;
    }

    public void setOneLevelDistrict(String oneLevelDistrict) {
        this.oneLevelDistrict = oneLevelDistrict;
    }

    public String getTwoLevelRegion() {
        return twoLevelRegion;
    }

    public void setTwoLevelRegion(String twoLevelRegion) {
        this.twoLevelRegion = twoLevelRegion;
    }

    public String getHouseType() {
        return houseType;
    }

    public void setHouseType(String houseType) {
        this.houseType = houseType;
    }

    public String getSourceFrom() {
        return sourceFrom;
    }

    public void setSourceFrom(String sourceFrom) {
        this.sourceFrom = sourceFrom;
    }

    public String getRentType() {
        return rentType;
    }

    public void setRentType(String rentType) {
        this.rentType = rentType;
    }

    public String getRoomType() {
        return roomType;
    }

    public void setRoomType(String roomType) {
        this.roomType = roomType;
    }

    public String getGenderLimit() {
        return genderLimit;
    }

    public void setGenderLimit(String genderLimit) {
        this.genderLimit = genderLimit;
    }

    public String getHouseLink() {
        return houseLink;
    }

    public void setHouseLink(String houseLink) {
        this.houseLink = houseLink;
    }

    public String getTopicDesc() {
        return topicDesc;
    }

    public void setTopicDesc(String topicDesc) {
        this.topicDesc = topicDesc;
    }

    public String getPostDate() {
        return postDate;
    }

    public void setPostDate(String postDate) {
        this.postDate = postDate;
    }

    public String getHouseCity() {
        return houseCity;
    }

    public void setHouseCity(String houseCity) {
        this.houseCity = houseCity;
    }

    public String getMetroNearBy() {
        return metroNearBy;
    }

    public void setMetroNearBy(String metroNearBy) {
        this.metroNearBy = metroNearBy;
    }
}
